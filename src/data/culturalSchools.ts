export const getCulturalSchools = (): CulturalSchool[] => {
  return culturalSchools;
};

export const getCulturalSchoolById = (id: string): CulturalSchool | undefined => {
  return culturalSchools.find((school) => school.id === id);
};

export const getCulturalSchoolsByCategory = (category: CulturalCategory): CulturalSchool[] => {
  return culturalSchools
    .filter((school) => school.category === category)
    .sort((a, b) => a.yearStart - b.yearStart);
};

export const getRootSchools = (category: CulturalCategory): CulturalSchool[] => {
  return culturalSchools
    .filter((school) => school.category === category && school.parentSchoolIds.length === 0)
    .sort((a, b) => a.yearStart - b.yearStart);
};

export const getChildSchools = (schoolId: string): CulturalSchool[] => {
  return culturalSchools
    .filter((school) => school.parentSchoolIds.includes(schoolId))
    .sort((a, b) => a.yearStart - b.yearStart);
};

export const getInfluencedSchools = (schoolId: string): CulturalSchool[] => {
  return culturalSchools
    .filter((school) => school.influencedSchoolIds.includes(schoolId))
    .sort((a, b) => a.yearStart - b.yearStart);
};
