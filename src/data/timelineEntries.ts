export function getWarEra(year: number): 'pre-war' | 'war' | 'interwar' | 'wwii' {
  if (year < 1914) return 'pre-war';
  if (year >= 1914 && year <= 1918) return 'war';
  if (year > 1918 && year < 1939) return 'interwar';
  return 'wwii';
}
